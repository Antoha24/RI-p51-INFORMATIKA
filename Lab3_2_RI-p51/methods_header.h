#ifndef METHODS_HEADER_H
#define METHODS_HEADER_H

#include <stdio.h>

/*
 * Функція: func
 * ----------------------------
 *  Обчислює значення заданої нелінійної функції f(x).
 *
 *  x : значення аргументу
 *
 *  returns: значення функції f(x)
 */
double func(double x);

/*
 * Функція: derivative
 * ----------------------------
 *  Обчислює значення похідної функції f'(x).
 *  Використовується для підтвердження існування лише одного кореня
 *  на заданому проміжку [a, b].
 *
 *  x : значення аргументу
 *
 *  returns: значення f'(x)
 */
double derivative(double x);

/*
 * Функція: bisection
 * ----------------------------
 *  Реалізує метод половинного ділення (бісекції)
 *  для знаходження кореня нелінійного рівняння f(x) = 0
 *  на проміжку [a, b].
 *
 *  a         : ліва межа інтервалу
 *  b         : права межа інтервалу
 *  eps       : задана похибка обчислення
 *  max_iter  : максимальна кількість ітерацій
 *  debug     : 1 — виводити деталі кожної ітерації, 0 — лише результат
 *
 *  returns: наближене значення кореня рівняння
 */
double bisection(double a, double b, double eps, int max_iter, int debug);

/*
 * Функція: chord
 * ----------------------------
 *  Реалізує метод хорд для знаходження кореня нелінійного рівняння f(x) = 0
 *  на проміжку [a, b].
 *
 *  a         : ліва межа інтервалу
 *  b         : права межа інтервалу
 *  eps       : задана похибка обчислення
 *  max_iter  : максимальна кількість ітерацій
 *  debug     : 1 — виводити деталі кожної ітерації, 0 — лише результат
 *
 *  returns: наближене значення кореня рівняння
 */
double chord(double a, double b, double eps, int max_iter, int debug);

#endif
